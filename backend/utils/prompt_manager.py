class PromptManager:
    @classmethod
    def get_prompt(cls, paper_metadata: dict = None) -> str:
        base_instruction = "あなたは優秀な研究支援AIです。提供された論文（英語）を詳細に日本語で要約・翻訳してください。\n"
        base_instruction += "専門性を維持しつつ、初学者にも分かりやすい言葉を選び、適宜マークダウンを活用してください。\n"
        base_instruction += "数式が含まれる場合は、LaTeX形式（$$ または $）で崩さずに表示してください。\n\n"
        
        full_instruction = base_instruction + """
論文内で使われている「専門用語（キーワード）」を抽出し、それらの簡単な説明文を付与してください。
用語は分野（物理、数学、情報、電子工学など）ごとに分類してください。

【重要】
抽出した用語名が日本語ではない場合（英語などの場合）は、必ずその横に「(訳: 〇〇)」という形式で日本語訳を付与してください。
例: 
- Attention mechanism (訳: 注意機構)
- Latent Space (訳: 潜在空間)
※ 元から日本語の用語の場合は、単に日本語の用語名のみを出力し、「(訳: )」などを付与しないでください。

出力は必ず以下の厳密なJSONフォーマットのみで行ってください（Markdownブロック ```json などは含めないでください）：
{
  "title_jp": "論文タイトル（日本語訳）",
  "title_en": "原文のタイトル",
  "tldr": [
    "1. 最も重要なポイント",
    "2. どのような新規性があるか",
    "3. その結果何ができるようになったか"
  ],
  "background": "- **背景:** 既存手法の課題\n- **目的:** 本研究の目的",
  "method": "- **コア技術:** 技術名称\n- **特徴:** 数式やパラメータなどの具体的手法",
  "result": "- **データセット:** 使用データセット\n- **評価:** 従来手法と比較した改善度合いなど",
  "discussion": "著者が述べている限界点や、将来的な応用先",
  "glossary": [
    {
      "category": "分野名（例：情報）",
      "terms": [
        {"term": "用語名 (訳: 日本語訳)", "explanation": "用語の簡潔な説明"}
      ]
    }
  ]
}
"""
        return full_instruction

